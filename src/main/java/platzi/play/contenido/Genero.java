//Los ENUM son clases especiales que representan un conjunto de constantes (valores fijos e inmutables).
//Se utilizan cuando se tiene un conjunto limitado y conocido de valores que una variable puede tomar.
//Por ejemplo, los días de la semana, los meses del año, los estados de un proceso, etc.
//Los ENUM mejoran la legibilidad del código y ayudan a evitar errores al restringir los valores posibles.
//Además, los ENUM pueden tener métodos y atributos, lo que los hace más poderosos que las simples constantes.


